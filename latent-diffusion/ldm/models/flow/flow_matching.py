class FlowMatchingWrapper(pl.LightningModule):
    def __init__(self, fm_model_config, conditioning_key):
        super().__init__()
        self.flowmatching_model = instantiate_from_config(fm_model_config)
        self.conditioning_key = conditioning_key
        assert self.conditioning_key in [None, 'concat', 'crossattn', 'hybrid', 'adm']

    def forward(self, t, x, c_concat: list = None, c_crossattn: list = None):
        if self.conditioning_key is None:
            out = self.flowmatching_model(t, x)
        elif self.conditioning_key == 'concat':
            xc = torch.cat([x] + c_concat, dim=1)
            out = self.flowmatching_model(t, xc)
        elif self.conditioning_key == 'crossattn':
            cc = torch.cat(c_crossattn, 1)
            out = self.flowmatching_model(t, x, context=cc)
        elif self.conditioning_key == 'hybrid':
            xc = torch.cat([x] + c_concat, dim=1)
            cc = torch.cat(c_crossattn, 1)
            out = self.flowmatching_model(t, xc, context=cc)
        elif self.conditioning_key == 'adm':
            cc = c_crossattn[0]
            out = self.flowmatching_model(t, x, y=cc)
        else:
            raise NotImplementedError()
        return out


